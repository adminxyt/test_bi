package com.yzy.bi.entiy;

public class Sk {

	private String sy;

	private String kzz;

	public String getSy() {
		return sy;
	}

	public void setSy(String sy) {
		this.sy = sy;
	}

	public String getKzz() {
		return kzz;
	}

	public void setKzz(String kzz) {
		this.kzz = kzz;
	}

	public Sk(String sy, String kzz) {
		super();
		this.sy = sy;
		this.kzz = kzz;
	}

	public Sk() {
		super();
	}

	public Sk(String sy) {
		super();
		this.sy = sy;
	}

}
